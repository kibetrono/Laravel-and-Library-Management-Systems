			<p>Once you have all those gadgets, you need to set up your apiary. Look for a place that is less disturbed,far from noise or human activities. This place should be well-drained lest the bees abscond due to high humidity. Avoid fields frequently sprayed with chemical pesticides to reduce bee poisoning and honey contamination; thus, if a must, use bee friendly pesticides.The apiary should also be close to water and nectar sources</p>
			<h3 style="color: green">Absconding</h3>
			<p>Absconding is caused by several factors that include lack of food and water. Offer the bees sugar syrup or provide little honey and water during drought. Frequent attacks by pests are also dangerous to a colony. Careless handling also frustrates bees and may make them abscond.Bees are best handled in the cool evenings. Remain confident and don’t crush the insects. If by mistake they get into you,walk away and remove them plus the stings by scraping off using your nails. Do not squeeze lest the venom spread into your body.You will always come in contact with bees during hive inspection or honey harvesting. During these times, wear a full bee suit and avoid woollen clothes that bees stick on</p>
