						<h3 style="color: green">PEST & DISEASE MANAGEMENT</h3>
						<p>Pests</p>
						<ul>
							<li>Aphids : These are soft bodied insects, which are green, black or brown and suck plant sap. Infested leaves curl and crinkle</li>
							<li>Control: Spray KINGCODE ELITE 50EC 10ml/20L or PENTAGON 50EC 10ml/20L or LOYALTY 700WDG 5g/20L</li>
							<li>Mealy bugs: These are small, flat, soft bodied insects with their bodies covered with a whitish secretion. They suck sap from tender leaves, petioles and fruits</li>
							<li>Control:Spray KINGCODE ELITE 50EC 10ml/20L or PRESENTO 200SP 10g/20L or EMERALD 200SL 10ml/20L</li>
							<li>Cutworms: These are found in the soil near the root zone of the crop. They cut the stem of younger plants below the soil surface. The attacked plants eventually withers</li>
							<li>control:Drench soil with PROFILE 440EC 60ml/20L or PENTAGON 50EC 20ml/20L</li>
							<li>Fruit fly: It attacks the young fruits while the rind is still tender altering their cell development and multiplication. This leads to production of deformed fruits as well as immature dropping of the infested fruits</li>
							<li>Control:Spray KINGCODE ELITE 50EC 10ml/20L or PRESENTO 200SP 10g/20L or PENTAGON 50EC 10ml/20L</li>
							<li>Mites: These have piercing and sucking mouthparts. They feed on the underside of the leaf leading to formation of whitish or yellowish patches. Heavy infestations can cause defoliation.</li>
							<li>Control:Spray ALONZE 50EC 5ml/20L or BAZOOKA 18EC 10ml/20L</li>
							<li>Scales:These are sucking insects found on green fruits and leaves. They appear as small shells glued to the plant and suck sap .Infested leaves turn yellow and may drop</li>
							<li>Control: Spray LOYALTY 700WDG 5g/20l or EMERALD 200SL 10ml/20L</li>
							<li>Spray JAMBO CLEAN 100ml/20L to get rid of the sooty mold</li>
							
						</ul>
